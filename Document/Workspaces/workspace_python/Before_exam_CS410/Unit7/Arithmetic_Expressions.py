def printUnary_op():
  result = (-3)
  print(result)

def printBinary_op():
  result = ("2+1")
  print(result)

def printTernary_op():
  result = "True" if 1<2 else "False"
  print(result)

printUnary_op()
printBinary_op()
printTernary_op()



